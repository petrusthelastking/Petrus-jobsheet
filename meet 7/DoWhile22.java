public class DoWhile22 {
    public static void main(String[] args) {
        int x = 0;
        do {
            System.out.println(x);
        } while (++x <= 8);
        System.out.println("Program berhenti");
        int r = 10;
        do {
            System.out.println(x);
        } while (++r <= 8);
        System.out.println("Program berhenti");
    }
}